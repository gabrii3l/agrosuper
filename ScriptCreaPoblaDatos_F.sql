/* CREACIÓN DE TABLAS */
CREATE TABLE ARRIENDO_CAMION 
(id_arriendo INT NOT NULL CONSTRAINT PK_ARRIENDO_CAMION PRIMARY KEY identity(1,1),
 nro_patente VARCHAR(6) NOT NULL,
 numrun_cli INT NOT NULL,
 fecha_ini_arriendo DATE NOT NULL,
 dias_solicitados INT,
 fecha_devolucion DATE);


CREATE TABLE TIPO_SALUD  
(id_tipo_sal  VARCHAR (3) NOT NULL CONSTRAINT PK_TIPO_SALUD PRIMARY KEY,
descripcion VARCHAR (100) NOT NULL,
pct_sueldo  INT NOT NULL);


CREATE TABLE CLIENTE
(numrun_cli INT NOT NULL CONSTRAINT PK_CLIENTE PRIMARY KEY,
 dvrun_cli VARCHAR(1) NOT NULL,
 appaterno_cli VARCHAR(30) NOT NULL,
 apmaterno_cli VARCHAR(30) NOT NULL,
 pnombre_cli VARCHAR(30) NOT NULL,
 snombre_cli VARCHAR(30),
 direccion VARCHAR(60) NOT NULL,
 celular_cli INT,
 fono_fijo_cli INT, 
 renta INT NOT NULL,
 fecha_nac_cli DATE,
 id_comuna INT,
 id_tipo_cli VARCHAR(1) NOT NULL,
 id_estado_civil INT NOT NULL);


CREATE TABLE COMUNA
(id_comuna INT NOT NULL CONSTRAINT PK_COMUNA PRIMARY KEY IDENTITY(1,1),
 nombre_comuna VARCHAR(30) NOT NULL CONSTRAINT UN_COMUNA_NOMCOMUNA UNIQUE);


CREATE TABLE EMPLEADO
(numrun_emp INT NOT NULL CONSTRAINT PK_EMPLEADO PRIMARY KEY ,
 dvrun_emp VARCHAR(1) NOT NULL,
 appaterno_emp VARCHAR(30) NOT NULL,
 apmaterno_emp VARCHAR(30) NOT NULL,
 pnombre_emp VARCHAR(25) NOT NULL,
 snombre_emp VARCHAR(25),
 direccion_emp VARCHAR(60) NOT NULL,
 sexo CHAR(1) NOT NULL CONSTRAINT CHK_SEXO CHECK (sexo IN ('F','M')),
 celular_emp INT,
 fono_fijo_emp INT,
 fecha_nac DATE NOT NULL,
 fecha_contrato DATE NOT NULL,
 sueldo_base INT NOT NULL,
 id_comuna INT,
 id_tipo_sal VARCHAR(3) NOT NULL,
 id_estado_civil INT NOT NULL);


CREATE TABLE MARCA
(id_marca INT NOT NULL CONSTRAINT PK_MARCA PRIMARY KEY IDENTITY(1,1),
 nombre_marca VARCHAR(20) NOT NULL);


CREATE TABLE TIPO_CLIENTE
(id_tipo_cli VARCHAR(1) NOT NULL CONSTRAINT PK_TIPO_CLIENTE PRIMARY KEY  ,
 nombre_tipo_cli VARCHAR(15) NOT NULL);


CREATE TABLE TIPO_CAMION
(id_tipo_camion VARCHAR(1) NOT NULL CONSTRAINT PK_TIPO_CAMION PRIMARY KEY,
 nombre_tipo_camion VARCHAR(40) NOT NULL);


CREATE TABLE CAMION 
(nro_patente VARCHAR(6) NOT NULL CONSTRAINT PK_CAMION PRIMARY KEY,
 color VARCHAR(15) NOT NULL,
 motor VARCHAR(5) NOT NULL,
 anio  INT,
 valor_arriendo_dia INT NOT NULL,
 valor_garantia_dia INT,
 id_tipo_camion VARCHAR NOT NULL,
 numrun_emp INT,
 id_marca INT NOT NULL);

CREATE TABLE ESTADO_CIVIL 
(id_estado_civil INT NOT NULL CONSTRAINT ESTADO_CIVIL_PK PRIMARY KEY IDENTITY(1,1),
 nombre_estado_civil VARCHAR(25) NOT NULL);

